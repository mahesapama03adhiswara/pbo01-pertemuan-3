package percabangan;

public class Switch {
    public static void main(String[] args) {
        int menuMakanan = 3;
        switch (menuMakanan){
            case 1 :
                System.out.println("nasi goreng");
                break;
            case 2 :
                System.out.println("mie goreng");
                break;
            case 3 :
                System.out.println("mie kuah");
                break;
            case 4 :
                System.out.println("mie ayam");
                break;
            case 5 :
                System.out.println("bubur ayam");
        }

    }
}
